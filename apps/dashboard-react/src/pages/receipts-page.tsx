export function ReceiptsPage() {
  const selectedBranch = useUiStore((state) => state.selectedBranch)
  const receiptsQuery = useReceipts(selectedBranch)
  const receipts = useMemo(() => receiptsQuery.data ?? [], [receiptsQuery.data])
  const birSettingsQuery = useQuery({
    queryKey: ['bir-settings', selectedBranch],
    queryFn: () => fetchBirSettings(selectedBranch),
    enabled: selectedBranch !== 'all',
  })
  const managedUsersQuery = useQuery({
    queryKey: ['managed-users', selectedBranch],
    queryFn: () => fetchManagedUsers(selectedBranch),
  })
  const managedUsers = managedUsersQuery.data ?? []

  const [draftFilters, setDraftFilters] = useState<ReceiptFilters>(initialFilters)
  const [appliedFilters, setAppliedFilters] = useState<ReceiptFilters>(initialFilters)
  const [selectedReceiptId, setSelectedReceiptId] = useState<string | null>(null)
  const [downloadingAuditTrail, setDownloadingAuditTrail] = useState(false)

  const cashierOptions = useMemo(
    () =>
      buildCashierOptions({
        receipts,
        managedUsers,
        extraValues: [
          draftFilters.cashierName !== 'ALL' ? draftFilters.cashierName : '',
          appliedFilters.cashierName !== 'ALL' ? appliedFilters.cashierName : '',
        ],
      }),
    [appliedFilters.cashierName, draftFilters.cashierName, managedUsers, receipts],
  )

  const filteredReceipts = useMemo(() => {
    const searchTerm = appliedFilters.search.trim().toLowerCase()

    return receipts.filter((receipt) => {
      if (!matchesBusinessDate(receipt, appliedFilters.businessDate)) {
        return false
      }

      if (
        appliedFilters.paymentMethod !== 'ALL' &&
        receipt.paymentMethod !== appliedFilters.paymentMethod
      ) {
        return false
      }

      if (appliedFilters.status !== 'ALL' && receipt.status !== appliedFilters.status) {
        return false
      }

      if (
        appliedFilters.cashierName !== 'ALL' &&
        normalizeCashierName(receipt.cashierName, managedUsers) !==
          appliedFilters.cashierName
      ) {
        return false
      }

      if (!searchTerm) {
        return true
      }

      return [
        formatReceiptNumber(receipt),
        receipt.refNumber,
        receipt.customerName ?? '',
        normalizeCashierName(receipt.cashierName, managedUsers),
        receipt.terminalName ?? '',
      ]
        .join(' ')
        .toLowerCase()
        .includes(searchTerm)
    })
  }, [appliedFilters, managedUsers, receipts])

  const selectedReceipt = useMemo(() => {
    if (filteredReceipts.length === 0) {
      return null
    }

    if (selectedReceiptId) {
      const selected = filteredReceipts.find((receipt) => receipt.id === selectedReceiptId)
      if (selected) {
        return selected
      }
    }

    return filteredReceipts[0]
  }, [filteredReceipts, selectedReceiptId])

  const receiptStats = useMemo(() => {
    const base = {
      total: filteredReceipts.length,
      completed: 0,
      refunded: 0,
      voided: 0,
    }

    return filteredReceipts.reduce((stats, receipt) => {
      if (receipt.status === 'COMPLETED') {
        stats.completed += 1
      }

      if (receipt.status === 'REFUNDED' || receipt.status === 'RETURNED') {
        stats.refunded += 1
      }

      if (receipt.status === 'VOID') {
        stats.voided += 1
      }

      return stats
    }, base)
  }, [filteredReceipts])

  const handleViewReceipt = (receiptId: string) => {
    setSelectedReceiptId(receiptId)
  }

  const handleReprintReceipt = (receiptId: string) => {
    setSelectedReceiptId(receiptId)

    if (typeof window !== 'undefined') {
      window.print()
    }
  }

  return (
    <div className="space-y-6 text-[color:var(--ink)]">
      <section className="glass-panel dashboard-panel-hover p-5 sm:p-6 md:p-7">
        <div className="flex flex-col gap-4 lg:flex-row lg:items-center lg:justify-between">
          <div>
            <h1 className="text-2xl font-bold tracking-tight text-[color:var(--ink)] sm:text-3xl">
              Receipt Management
            </h1>
            <p className="mt-1 text-sm text-[color:var(--muted)]">
              Search, review, reprint, refund, and track POS receipts across all terminals.
            </p>
          </div>
          <div className="flex flex-col gap-3 sm:flex-row sm:flex-wrap">
            <button
              type="button"
              onClick={() =>
                downloadReceiptsCsv(filteredReceipts, {
                  selectedBranch,
                  birSettings: birSettingsQuery.data,
                })
              }
              className="w-full rounded-2xl border border-[color:var(--border)] bg-[color:var(--panel)] px-4 py-2.5 text-sm font-medium text-[color:var(--muted)] shadow-sm transition hover:border-slate-300 hover:bg-[color:var(--surface-soft)] sm:w-auto"
            >
              Export CSV
            </button>
            <button
              type="button"
              disabled={downloadingAuditTrail}
              onClick={async () => {
                setDownloadingAuditTrail(true)
                try {
                  await downloadAuditTrailCsv({
                    selectedBranch,
                    birSettings: birSettingsQuery.data,
                  })
                } finally {
                  setDownloadingAuditTrail(false)
                }
              }}
              className="w-full rounded-2xl border border-[color:var(--border)] bg-[color:var(--panel)] px-4 py-2.5 text-sm font-medium text-[color:var(--muted)] shadow-sm transition hover:border-slate-300 hover:bg-[color:var(--surface-soft)] disabled:opacity-50 sm:w-auto"
            >
              {downloadingAuditTrail ? 'Exporting…' : 'Export Audit Trail'}
            </button>
            <button
              type="button"
              onClick={() => {
                if (typeof window !== 'undefined') {
                  window.print()
                }
              }}
              className="w-full rounded-2xl bg-slate-900 px-4 py-2.5 text-sm font-medium text-white shadow-sm transition hover:bg-slate-800 sm:w-auto"
            >
              Download PDF
            </button>
          </div>
        </div>
      </section>

      <section className="grid gap-4 sm:grid-cols-2 2xl:grid-cols-4">
        <ReceiptStatCard
          label="Today's Receipts"
          value={countFormatter.format(receiptStats.total)}
          gradient="from-[#52d4cc] to-[#28a89a]"
          icon={FileText}
        />
        <ReceiptStatCard
          label="Completed"
          value={countFormatter.format(receiptStats.completed)}
          gradient="from-[#4ade80] to-[#16a34a]"
          icon={CheckCircle2}
        />
        <ReceiptStatCard
          label="Refunded"
          value={countFormatter.format(receiptStats.refunded)}
          gradient="from-[#ff9b6a] to-[#f06040]"
          icon={Undo2}
        />
        <ReceiptStatCard
          label="Voided"
          value={countFormatter.format(receiptStats.voided)}
          gradient="from-[#fb7185] to-[#e11d48]"
          icon={Ban}
        />
      </section>

      <section className="glass-panel dashboard-panel-hover p-5">
        <div className="grid gap-3 md:grid-cols-2 2xl:grid-cols-6">
          <input
            value={draftFilters.search}
            onChange={(event) =>
              setDraftFilters((current) => ({ ...current, search: event.target.value }))
            }
            className="rounded-2xl border border-[color:var(--border)] bg-[color:var(--panel)] px-4 py-3 text-sm text-[color:var(--ink)] outline-none transition placeholder:text-[color:var(--muted)] focus:border-blue-400 focus:ring-2 focus:ring-blue-100"
            placeholder="Search receipt #, customer, cashier"
          />
          <input
            value={draftFilters.businessDate}
            onChange={(event) =>
              setDraftFilters((current) => ({ ...current, businessDate: event.target.value }))
            }
            className="rounded-2xl border border-[color:var(--border)] bg-[color:var(--panel)] px-4 py-3 text-sm text-[color:var(--ink)] outline-none transition focus:border-blue-400 focus:ring-2 focus:ring-blue-100"
            type="date"
          />
          <select
            value={draftFilters.paymentMethod}
            onChange={(event) =>
              setDraftFilters((current) => ({
                ...current,
                paymentMethod: event.target.value as ReceiptFilters['paymentMethod'],
              }))
            }
            className="rounded-2xl border border-[color:var(--border)] bg-[color:var(--panel)] px-4 py-3 text-sm text-[color:var(--ink)] outline-none transition focus:border-blue-400 focus:ring-2 focus:ring-blue-100"
          >
            <option value="ALL">All Payment Methods</option>
            {Object.entries(paymentLabels).map(([value, label]) => (
              <option key={value} value={value}>
                {label}
              </option>
            ))}
          </select>
          <select
            value={draftFilters.status}
            onChange={(event) =>
              setDraftFilters((current) => ({
                ...current,
                status: event.target.value as ReceiptFilters['status'],
              }))
            }
            className="rounded-2xl border border-[color:var(--border)] bg-[color:var(--panel)] px-4 py-3 text-sm text-[color:var(--ink)] outline-none transition focus:border-blue-400 focus:ring-2 focus:ring-blue-100"
          >
            <option value="ALL">All Status</option>
            {Object.entries(statusLabels).map(([value, label]) => (
              <option key={value} value={value}>
                {label}
              </option>
            ))}
          </select>
          <select
            value={draftFilters.cashierName}
            onChange={(event) =>
              setDraftFilters((current) => ({
                ...current,
                cashierName: event.target.value,
              }))
            }
            className="rounded-2xl border border-[color:var(--border)] bg-[color:var(--panel)] px-4 py-3 text-sm text-[color:var(--ink)] outline-none transition focus:border-blue-400 focus:ring-2 focus:ring-blue-100"
          >
            <option value="ALL">All Cashiers</option>
            {cashierOptions.map((cashierName) => (
              <option key={cashierName} value={cashierName}>
                {cashierName}
              </option>
            ))}
          </select>
          <div className="flex flex-col gap-3 sm:flex-row">
            <button
              type="button"
              onClick={() => setAppliedFilters(draftFilters)}
              className="flex-1 rounded-2xl bg-blue-600 px-4 py-3 text-sm font-medium text-white transition hover:bg-blue-700"
            >
              Apply Filters
            </button>
            <button
              type="button"
              onClick={() => {
                setDraftFilters(initialFilters)
                setAppliedFilters(initialFilters)
              }}
              className="rounded-2xl border border-[color:var(--border)] px-4 py-3 text-sm font-medium text-[color:var(--muted)] transition hover:bg-[color:var(--surface-soft)]"
            >
              Reset
            </button>
          </div>
        </div>
      </section>

      <section className="grid gap-6 2xl:grid-cols-[minmax(0,1.45fr)_minmax(360px,1fr)]">
        <article className="min-w-0 glass-panel dashboard-panel-hover p-5">
          <div className="mb-4 flex flex-col gap-3 md:flex-row md:items-center md:justify-between">
            <div>
              <h2 className="text-xl font-semibold text-[color:var(--ink)]">Receipt List</h2>
              <p className="text-sm text-[color:var(--muted)]">
                Latest transactions from all terminals
              </p>
            </div>
            <button
              type="button"
              onClick={() => void receiptsQuery.refetch()}
              className="rounded-2xl border border-[color:var(--border)] px-4 py-2 text-sm font-medium text-[color:var(--muted)] transition hover:bg-[color:var(--surface-soft)]"
            >
              Refresh
            </button>
          </div>
          {receiptsQuery.isError ? (
            <div className="mb-4 rounded-2xl border border-rose-200 bg-rose-50 px-4 py-3 text-sm text-rose-700">
              Unable to load live receipts from the backend. Check API connection and branch filter.
            </div>
          ) : null}
          {receiptsQuery.isFetching ? (
            <p className="mb-3 text-xs text-[color:var(--muted)]">Syncing latest receipts...</p>
          ) : null}

          <div className="max-h-[680px] space-y-2 overflow-y-auto pr-1 2xl:hidden">
            {filteredReceipts.length === 0 ? (
              <div className="rounded-[24px] border border-[color:var(--border)] bg-[color:var(--surface-soft)] px-6 py-10 text-center text-sm text-[color:var(--muted)]">
                No receipts matched the current filters.
              </div>
            ) : (
              filteredReceipts.map((receipt) => (
                <ReceiptListCard
                  key={receipt.id}
                  receipt={receipt}
                  cashierName={normalizeCashierName(receipt.cashierName, managedUsers)}
                  isSelected={receipt.id === selectedReceipt?.id}
                  onView={handleViewReceipt}
                  onReprint={handleReprintReceipt}
                />
              ))
            )}
          </div>

          <div className="hidden overflow-hidden rounded-[24px] border border-[color:var(--border)] 2xl:block">
            <div className="max-h-[680px] overflow-auto overscroll-contain">
              <div className={cn(tableGridClass, 'sticky top-0 z-10 bg-[color:var(--header-tint)] px-4 py-3 text-xs font-semibold uppercase tracking-[0.22em] text-[color:var(--muted)]')}>
                <div>Receipt #</div>
                <div>Date/Time</div>
                <div>Customer</div>
                <div>Cashier</div>
                <div>Payment</div>
                <div>Status</div>
                <div>Total</div>
                <div>Actions</div>
              </div>

              {filteredReceipts.length === 0 ? (
                <div className="px-6 py-10 text-center text-sm text-[color:var(--muted)]">
                  No receipts matched the current filters.
                </div>
              ) : (
                filteredReceipts.map((receipt) => (
                  <div
                    key={receipt.id}
                    className={cn(
                      tableGridClass,
                      'border-t border-[color:var(--border)] px-4 py-3 text-sm transition',
                      receipt.id === selectedReceipt?.id ? 'bg-[color:var(--surface-soft)]' : 'bg-[color:var(--panel)]',
                    )}
                  >
                    <div className="font-semibold text-[color:var(--ink)]">
                      {formatReceiptNumber(receipt)}
                    </div>
                    <div className="text-[color:var(--muted)]">{formatReceiptDate(receipt.createdAt)}</div>
                    <div className="text-[color:var(--muted)]">
                      {receipt.customerName ?? 'Walk-in Customer'}
                    </div>
                    <div className="text-[color:var(--muted)]">
                      {normalizeCashierName(receipt.cashierName, managedUsers)}
                    </div>
                    <div className="text-[color:var(--muted)]">
                      {paymentLabels[receipt.paymentMethod]}
                    </div>
                    <div>
                      <span
                        className={cn(
                          'inline-flex rounded-full px-3 py-1 text-xs font-semibold',
                          statusClassNames[receipt.status],
                        )}
                      >
                        {statusLabels[receipt.status]}
                      </span>
                    </div>
                    <div className="font-semibold text-[color:var(--ink)]">{formatMoney(receipt.total)}</div>
                    <div className="flex flex-wrap gap-2">
                      <button
                        type="button"
                        onClick={() => handleViewReceipt(receipt.id)}
                        className="rounded-xl border border-[color:var(--border)] px-3 py-1 text-xs font-medium text-[color:var(--muted)] transition hover:bg-[color:var(--surface-soft)]"
                      >
                        View
                      </button>
                      <button
                        type="button"
                        onClick={() => handleReprintReceipt(receipt.id)}
                        className="rounded-xl border border-[color:var(--border)] px-3 py-1 text-xs font-medium text-[color:var(--muted)] transition hover:bg-[color:var(--surface-soft)]"
                      >
                        Reprint
                      </button>
                    </div>
                  </div>
                ))
              )}
            </div>
          </div>
        </article>

        <div className="space-y-6">
          <article className="glass-panel dashboard-panel-hover p-5 2xl:sticky 2xl:top-4">
            {selectedReceipt ? (
              <>
                <div className="mb-4 flex items-start justify-between gap-3">
                  <div>
                    <h2 className="text-xl font-semibold text-[color:var(--ink)]">Receipt Details</h2>
                    <p className="text-sm text-[color:var(--muted)]">Full transaction drill-down</p>
                  </div>
                  <span
                    className={cn(
                      'inline-flex rounded-full px-3 py-1 text-xs font-semibold',
                      statusClassNames[selectedReceipt.status],
                    )}
                  >
                    {statusLabels[selectedReceipt.status]}
                  </span>
                </div>

                <div className="grid grid-cols-1 gap-3 sm:grid-cols-2">
                  <ReceiptInfoTile
                    label="Receipt #"
                    value={formatReceiptNumber(selectedReceipt)}
                  />
                  <ReceiptInfoTile
                    label="Date/Time"
                    value={formatReceiptDate(selectedReceipt.createdAt)}
                  />
                  <ReceiptInfoTile
                    label="Cashier"
                    value={normalizeCashierName(selectedReceipt.cashierName, managedUsers)}
                  />
                  <ReceiptInfoTile
                    label="Terminal"
                    value={selectedReceipt.terminalName ?? selectedReceipt.terminalId}
                  />
                  <ReceiptInfoTile
                    label="Customer"
                    value={selectedReceipt.customerName ?? 'Walk-in Customer'}
                  />
                  <ReceiptInfoTile
                    label="Customer TIN"
                    value={selectedReceipt.customerTin ?? 'Not provided'}
                  />
                  <ReceiptInfoTile
                    label="Business style"
                    value={selectedReceipt.customerBusinessStyle ?? 'Not provided'}
                  />
                  <ReceiptInfoTile
                    label="Payment"
                    value={paymentLabels[selectedReceipt.paymentMethod]}
                  />
                </div>

                <div className="mt-4 rounded-2xl bg-[color:var(--surface-soft)] p-4">
                  <p className="text-sm text-[color:var(--muted)]">Customer address</p>
                  <p className="mt-1 text-sm font-medium text-[color:var(--ink)]">
                    {selectedReceipt.customerAddress ?? 'Not provided'}
                  </p>
                </div>

                <div className="mt-5 overflow-hidden rounded-2xl border border-[color:var(--border)]">
                  <div className="border-b border-[color:var(--border)] px-4 py-3 text-sm font-semibold text-[color:var(--ink)]">
                    Items Purchased
                  </div>
                  <div className="space-y-3 px-4 py-4">
                    {(selectedReceipt.items ?? []).map((item) => (
                      <div key={item.name} className="flex items-center justify-between gap-4 text-sm">
                        <div>
                          <p className="font-medium text-[color:var(--ink)]">{item.name}</p>
                          <p className="text-[color:var(--muted)]">Qty: {item.qty}</p>
                        </div>
                        <p className="font-semibold text-[color:var(--ink)]">
                          {formatMoney(item.qty * item.price)}
                        </p>
                      </div>
                    ))}
                  </div>
                </div>

                <div className="mt-5 space-y-2 rounded-2xl bg-[color:var(--surface-soft)] p-4 text-sm">
                  <div className="flex justify-between gap-4">
                    <span className="text-[color:var(--muted)]">Subtotal</span>
                    <span className="font-medium text-[color:var(--ink)]">
                      {formatMoney(selectedReceipt.subtotal ?? selectedReceipt.total)}
                    </span>
                  </div>
                  <div className="flex justify-between gap-4">
                    <span className="text-[color:var(--muted)]">VAT</span>
                    <span className="font-medium text-[color:var(--ink)]">
                      {formatMoney(selectedReceipt.vatAmount ?? 0)}
                    </span>
                  </div>
                  <div className="flex justify-between gap-4">
                    <span className="text-[color:var(--muted)]">Discount</span>
                    <span className="font-medium text-[color:var(--ink)]">
                      {formatMoney(selectedReceipt.discountAmount ?? 0)}
                    </span>
                  </div>
                  <div className="flex justify-between gap-4">
                    <span className="text-[color:var(--muted)]">Change</span>
                    <span className="font-medium text-[color:var(--ink)]">
                      {formatMoney(selectedReceipt.changeAmount ?? 0)}
                    </span>
                  </div>
                  <div className="flex justify-between gap-4 border-t border-[color:var(--border)] pt-2 text-base font-semibold text-[color:var(--ink)]">
                    <span>Total</span>
                    <span>{formatMoney(selectedReceipt.total)}</span>
                  </div>
                </div>

                <div className="mt-5 flex flex-col gap-3 sm:flex-row sm:flex-wrap">
                  <button
                    type="button"
                    onClick={() => {
                      if (typeof window !== 'undefined') {
                        window.print()
                      }
                    }}
                    className="rounded-2xl bg-slate-900 px-4 py-2 text-sm font-medium text-white transition hover:bg-slate-800"
                  >
                    Reprint Receipt
                  </button>
                  <button
                    type="button"
                    className="rounded-2xl bg-amber-500 px-4 py-2 text-sm font-medium text-white transition hover:bg-amber-600"
                  >
                    Process Refund
                  </button>
                  <button
                    type="button"
                    className="rounded-2xl border border-[color:var(--border)] px-4 py-2 text-sm font-medium text-[color:var(--muted)] transition hover:bg-[color:var(--surface-soft)]"
                  >
                    Send to Email
                  </button>
                </div>
              </>
            ) : (
              <div className="rounded-2xl bg-[color:var(--surface-soft)] px-5 py-8 text-sm text-[color:var(--muted)]">
                Select a receipt from the list to inspect the full transaction details.
              </div>
            )}
          </article>

          <article className="glass-panel dashboard-panel-hover p-5">
            <h2 className="text-xl font-semibold text-[color:var(--ink)]">Audit Trail</h2>
            <p className="mb-4 text-sm text-[color:var(--muted)]">
              Every action linked to this receipt
            </p>
            <div className="space-y-3">
              {(selectedReceipt?.timeline ?? []).map((entry) => (
                <div
                  key={entry}
                  className="rounded-2xl bg-[color:var(--surface-soft)] p-3 text-sm text-[color:var(--muted)]"
                >
                  {entry}
                </div>
              ))}
            </div>
          </article>
        </div>
      </section>
    </div>
  )
}




