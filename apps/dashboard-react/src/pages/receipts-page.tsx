const SOFTWARE_NAME = 'BIGTIME POS'
const SOFTWARE_VERSION = '1.0.0'

const DEFAULT_RECEIPT_DATE = ''

const currencyFormatter = new Intl.NumberFormat('en-PH', {
  style: 'currency',
  currency: 'PHP',
  minimumFractionDigits: 2,
})

const countFormatter = new Intl.NumberFormat('en-PH')

const tableGridClass =
  'grid min-w-[980px] grid-cols-[minmax(160px,1.35fr)_minmax(160px,1.1fr)_minmax(150px,1.1fr)_minmax(140px,1fr)_minmax(110px,0.9fr)_minmax(120px,0.9fr)_minmax(130px,0.9fr)_minmax(145px,1fr)] gap-3'

type ReceiptFilters = {
  search: string
  businessDate: string
  paymentMethod: 'ALL' | PaymentMethod
  status: 'ALL' | TransactionStatus
  cashierName: 'ALL' | string
}

const initialFilters: ReceiptFilters = {
  search: '',
  businessDate: DEFAULT_RECEIPT_DATE,
  paymentMethod: 'ALL',
  status: 'ALL',
  cashierName: 'ALL',
}

const paymentLabels: Record<PaymentMethod, string> = {
  CASH: 'Cash',
  CARD: 'Card',
  GCASH: 'GCash',
  MAYA: 'Maya',
  SPLIT: 'Split',
}

const statusLabels: Record<TransactionStatus, string> = {
  COMPLETED: 'Completed',
  VOID: 'Voided',
  RETURNED: 'Returned',
  REFUNDED: 'Refunded',
  HELD: 'Held',
}

const statusClassNames: Record<TransactionStatus, string> = {
  COMPLETED: 'bg-emerald-100 text-emerald-700 ring-1 ring-emerald-200',
  VOID: 'bg-red-100 text-red-700 ring-1 ring-red-200',
  RETURNED: 'bg-amber-100 text-amber-700 ring-1 ring-amber-200',
  REFUNDED: 'bg-amber-100 text-amber-700 ring-1 ring-amber-200',
  HELD: 'bg-[color:var(--border)] text-slate-700 ring-1 ring-slate-300',
}

function formatMoney(value: number) {
  return currencyFormatter.format(value)
}

function formatReceiptNumber(receipt: ReceiptRecord) {
  return `SI ${String(receipt.orNumber).padStart(6, '0')}`
}

function formatReceiptDate(value: string) {
  const date = new Date(value)
  const datePart = date.toLocaleDateString('sv-SE')
  const timePart = date.toLocaleTimeString('en-PH', {
    hour: '2-digit',
    minute: '2-digit',
  })

  return `${datePart} ${timePart}`
}

function matchesBusinessDate(receipt: ReceiptRecord, businessDate: string) {
  if (!businessDate) {
    return true
  }

  return receipt.createdAt.slice(0, 10) === businessDate
}

function csvCell(value: unknown) {
  return `"${String(value ?? '').replaceAll('"', '""')}"`
}

function csvRow(values: unknown[]) {
  return values.map(csvCell).join(',')
}

function buildBirMetadataRows(
  selectedBranch: string,
  birSettings?: BirSettingsResponse,
) {
  const branchLabel =
    selectedBranch === 'all'
      ? 'All Branches'
      : birSettings?.storeName.trim() || selectedBranch
  const address =
    birSettings?.businessAddressLines.filter(Boolean).join(', ') || 'Not configured'
  const tinLabel = birSettings?.vatTin.trim()
    ? `VAT REG TIN ${birSettings.vatTin.trim()}`
    : 'Not configured'
  const session = readAuthSession()
  const generatedBy = session
    ? `${session.user.name} (${session.user.employeeCode})`
    : 'Unknown'

  return [
    csvRow(['Report', 'Sales Invoice Register']),
    csvRow(['Software Name', SOFTWARE_NAME]),
    csvRow(['Software Version', SOFTWARE_VERSION]),
    csvRow([
      'Generated At',
      new Date().toLocaleString('en-PH', { hour12: true, timeZone: 'Asia/Manila' }),
    ]),
    csvRow(['Generated By', generatedBy]),
    csvRow(['Branch', branchLabel]),
    csvRow(['Registered Name', birSettings?.storeName.trim() || 'Not configured']),
    csvRow(['Registered Address', address]),
    csvRow(['TIN', tinLabel]),
    csvRow(['Permit Number', birSettings?.permitNumber.trim() || 'Not configured']),
    csvRow(['Permit Date Issued', birSettings?.permitDateIssued.trim() || 'Not configured']),
    csvRow([
      'Authority to Print Number',
      birSettings?.authorityToPrintNumber.trim() || 'Not configured',
    ]),
    csvRow([
      'Authority to Print Date Issued',
      birSettings?.authorityToPrintDateIssued.trim() || 'Not configured',
    ]),
    csvRow([
      'Approved Serial Range',
      birSettings?.approvedSerialRange.trim() || 'Not configured',
    ]),
    csvRow(['MIN', birSettings?.machineIdentificationNumber.trim() || 'Not configured']),
    csvRow(['Serial Number', birSettings?.serialNumber.trim() || 'Not configured']),
    '',
  ]
}

function downloadReceiptsCsv(
  receipts: ReceiptRecord[],
  options: {
    selectedBranch: string
    birSettings?: BirSettingsResponse
  },
) {
  if (typeof window === 'undefined') {
    return
  }

  const lines = [
    ...buildBirMetadataRows(options.selectedBranch, options.birSettings),
    csvRow([
      'Sales Invoice Number',
      'Date/Time',
      'Customer',
      'Customer TIN',
      'Customer Address',
      'Business Style',
      'Cashier',
      'Terminal',
      'Payment',
      'Status',
      'Subtotal',
      'Vatable Sales',
      'VAT Exempt Sales',
      'Zero Rated Sales',
      'VAT',
      'Discount',
      'Total',
    ]),
    ...receipts.map((receipt) =>
      csvRow([
        formatReceiptNumber(receipt),
        formatReceiptDate(receipt.createdAt),
        receipt.customerName ?? 'Walk-in Customer',
        receipt.customerTin ?? '',
        receipt.customerAddress ?? '',
        receipt.customerBusinessStyle ?? '',
        receipt.cashierName,
        receipt.terminalName ?? receipt.terminalId,
        paymentLabels[receipt.paymentMethod],
        statusLabels[receipt.status],
        receipt.subtotal ?? 0,
        receipt.vatableSales ?? 0,
        receipt.vatExemptSales ?? 0,
        receipt.zeroRatedSales ?? 0,
        receipt.vatAmount ?? 0,
        receipt.discountAmount ?? 0,
        receipt.total,
      ]),
    ),
  ]

  const blob = new Blob([lines.join('\n')], { type: 'text/csv;charset=utf-8' })
  const url = URL.createObjectURL(blob)
  const link = document.createElement('a')
  link.href = url
  link.download = 'bigtime-pos-receipts.csv'
  link.click()
  URL.revokeObjectURL(url)
}

async function downloadAuditTrailCsv(options: {
  selectedBranch: string
  birSettings?: BirSettingsResponse
}) {
  if (typeof window === 'undefined') {
    return
  }

  const params: Record<string, string> = {}
  if (options.selectedBranch !== 'all') {
    params['branchId'] = options.selectedBranch
  }

  const result = await fetchAuditTrail(params)
  const entries = result.items ?? []

  const lines = [
    ...buildBirMetadataRows(options.selectedBranch, options.birSettings),
    csvRow(['Report', 'Audit Trail Log']),
    '',
    csvRow(['ID', 'Branch', 'User ID', 'Action', 'Table', 'Record ID', 'Date/Time', 'Hash']),
    ...entries.map((entry) =>
      csvRow([
        entry.id,
        entry.branchId,
        entry.userId ?? '',
        entry.action,
        entry.tableName,
        entry.recordId,
        entry.createdAt,
        entry.hash,
      ]),
    ),
  ]

  const blob = new Blob([lines.join('\n')], { type: 'text/csv;charset=utf-8' })
  const url = URL.createObjectURL(blob)
  const link = document.createElement('a')
  link.href = url
  const ts = new Date().toISOString().slice(0, 10)
  link.download = `bigtime-pos-audit-trail-${ts}.csv`
  link.click()
  URL.revokeObjectURL(url)
}

